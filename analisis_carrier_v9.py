print("Generando pronósticos...")
def forecast_series(series, tipo, horizon=3):
    series = pd.Series(series).astype(float).fillna(0)
    if (series==0).all():
        return np.zeros(horizon)
    try:
        if tipo=='Smooth':
            fit = ExponentialSmoothing(series, trend=None, seasonal=None).fit()
            fcast = fit.forecast(horizon)
        elif tipo=='Erratic':
            fit = Holt(series).fit()
            fcast = fit.forecast(horizon)
        elif tipo=='Intermittent':
            fcast = CrostonClassic().forecast(y=series.to_numpy(), h=horizon)
            fcast = np.array(fcast['mean']).flatten()
        elif tipo=='Lumpy':
            fcast = CrostonSBA().forecast(y=series.to_numpy(), h=horizon)
            fcast = np.array(fcast['mean']).flatten()
        else:
            fcast = np.full(horizon, np.mean(series))
        if len(fcast) < horizon:
            fcast = np.pad(fcast, (0, horizon - len(fcast)), 'constant', constant_values=0)
        return fcast
    except:
        return np.full(horizon, np.mean(series))

forecast_records = []
horizon = 3

for idx, row in hist.iterrows():
    serie = data.loc[idx]
    tipo = row['Tipo_SB']
    fcast = forecast_series(serie, tipo, horizon)
    rec = {'Numero de parte': row[id_col], 'Tipo_SB': tipo, 'Modelo_Pronostico': row['Modelo_Pronostico']}
    for i in range(horizon):
        rec[f'Semana_+{i+1}'] = fcast[i]
    forecast_records.append(rec)

