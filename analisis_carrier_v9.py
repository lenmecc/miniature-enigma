# 3️⃣ CLASIFICACIÓN DE PATRONES DE DEMANDA (Syntetos–Boylan)
# ==========================================================
print("Clasificando demanda (Syntetos-Boylan)...")
id_col = 'Numero de parte'
data_cols = hist.columns.drop(id_col)
data = hist[data_cols]

def calc_metrics(row):
    series = row[data_cols]
    series_nonzero = series[series > 0]
    if len(series_nonzero) == 0:
        mean = 0
        cv = 0
    else:
        mean = series_nonzero.mean()
        cv = series_nonzero.std() / (mean + 1e-6)
    p = (series > 0).sum() / len(series)
    return pd.Series({'mean': mean, 'cv': cv, 'p0': p})

hist[['mean','cv','p0']] = hist.apply(calc_metrics, axis=1)

def classify_syntetos_boylan(row):
    cv2 = row['cv']**2
    p0 = row['p0']
    if cv2 <= 0.49 and p0 > 0.5:
        return 'Smooth'
    elif cv2 > 0.49 and p0 > 0.5:
        return 'Erratic'
    elif cv2 <= 0.49 and p0 <= 0.5:
        return 'Intermittent'
    else:
        return 'Lumpy'

hist['Tipo_SB'] = hist.apply(classify_syntetos_boylan, axis=1)
model_map = {'Smooth':'SES','Erratic':'Holt','Intermittent':'CrostonClassic','Lumpy':'CrostonSBA'}
hist['Modelo_Pronostico'] = hist['Tipo_SB'].map(model_map)

