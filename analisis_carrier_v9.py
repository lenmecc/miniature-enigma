def generar_plan_con_score(df_followup_source, df_hist, df_forecast_base, df_variacion, es_baseline=False):
    """
    Toma un dataframe de followup (viejo o nuevo) y calcula el plan
    completo con su score de riesgo proactivo.
    """
    id_col = 'Numero de parte'

    # 1. Consolidar el followup (por si hay duplicados)
    df_followup_merge = (
        df_followup_source.groupby(id_col, as_index=False)
        .agg({
            'Saldo': 'sum',
            'Fecha': 'min',
            "Class": "first",
            "Req": "sum"
        })
    )

    # 2. Merge con la base de pronósticos (S-B)
    df_plan = df_forecast_base.merge(df_followup_merge, on=id_col, how='left')

    # 3. Añadir 'cv' del histórico
    df_plan = df_plan.merge(
        df_hist[['Numero de parte', 'cv']],
        on='Numero de parte',
        how='left'
    )

    # 4. Identificar y consolidar partes nuevas (basado en el followup de entrada)
    nuevas_partes = set(df_followup_source[id_col]) - set(df_hist[id_col])
    df_nuevas = df_followup_source[df_followup_source[id_col].isin(nuevas_partes)].copy()

    dfnuevas_merge = (
        df_nuevas.groupby(id_col, as_index=False)
        .agg({
            'Saldo': 'sum',
            'Fecha': 'min',
            'Req': 'sum',
            'Class': 'first'
        })
    )
    dfnuevas_merge['Tipo_SB'] = 'Nueva'

    # 5. Unir partes con historia y nuevas
    df_plan_final = pd.concat([df_plan, dfnuevas_merge], ignore_index=True)
    df_plan_final['Tipo_demanda_o_nueva'] = df_plan_final['Tipo_SB'].fillna('Nueva')

    # Imputar CV
    cv_median = df_hist['cv'].median()
    df_plan_final['cv'] = df_plan_final['cv'].fillna(cv_median)

    # 6. Añadir variación de forecast (Deltas)
    if es_baseline:
        # Si es el plan 'Viernes', no hay 'nerviosismo'. Todo es 0.
        df_plan_final['delta_forecast_abs'] = 0
        df_plan_final['delta_forecast_pct'] = 0
    else:
        # Si es el plan 'Martes', usamos la variación calculada
        df_plan_final = df_plan_final.merge(
            df_variacion[['Numero de parte', 'delta_forecast_abs', 'delta_forecast_pct']],
            on='Numero de parte',
            how='left'
        )
        # Imputar deltas para partes que no estaban en df_variacion
        df_plan_final['delta_forecast_abs'] = df_plan_final['delta_forecast_abs'].fillna(0)
        df_plan_final['delta_forecast_pct'] = df_plan_final['delta_forecast_pct'].fillna(0)

    # 7. CÁLCULO DE RIESGO (Lógica de la Sección 7 original)
    hoy = pd.Timestamp.today()
    df_plan_final['Días_para_entrega'] = (df_plan_final['Fecha'] - hoy).dt.days
    df_plan_final['Saldo'] = df_plan_final['Saldo'].clip(lower=0).fillna(0)
    df_plan_final['Req'] = df_plan_final['Req'].clip(lower=0).fillna(0)

    # Normalización
    with np.errstate(invalid='ignore'): # Suprimir warnings de división por cero si Z-score es 0
        df_plan_final['norm_saldo'] = (zscore(df_plan_final['Saldo']).clip(-2, 2) + 2) / 4
        df_plan_final['norm_demanda'] = (zscore(df_plan_final['Req']).clip(-2, 2) + 2) / 4
        df_plan_final['norm_variabilidad'] = (zscore(df_plan_final['cv']).clip(-2, 2) + 2) / 4
        df_plan_final['norm_delta_forecast'] = (zscore(df_plan_final['delta_forecast_pct']).clip(-2, 2) + 2) / 4

    for c in ['norm_saldo', 'norm_demanda', 'norm_variabilidad', 'norm_delta_forecast']:
        df_plan_final[c] = df_plan_final[c].clip(0, 1).fillna(0)

    # Ponderaciones
    peso_tipo = {
        'Smooth':       {'saldo': 0.3, 'demanda': 0.3, 'variabilidad': 0.1, 'delta': 0.3},
        'Erratic':      {'saldo': 0.4, 'demanda': 0.2, 'variabilidad': 0.2, 'delta': 0.2},
        'Intermittent': {'saldo': 0.5, 'demanda': 0.2, 'variabilidad': 0.1, 'delta': 0.2},
        'Lumpy':        {'saldo': 0.6, 'demanda': 0.2, 'variabilidad': 0.1, 'delta': 0.1},
        'Nueva':        {'saldo': 0.4, 'demanda': 0.4, 'variabilidad': 0.0, 'delta': 0.2}
    }

    def calc_risk_proactivo(row):
        w = peso_tipo.get(row.get('Tipo_demanda_o_nueva', 'Smooth'))
        risk = (
            w['saldo'] * row['norm_saldo'] +
            w['demanda'] * row['norm_demanda'] +
            w['variabilidad'] * row['norm_variabilidad'] +
            w['delta'] * row['norm_delta_forecast']
        )
        return risk

    df_plan_final['score_prioridad_proactivo'] = df_plan_final.apply(calc_risk_proactivo, axis=1)

    df_plan_final['Nivel_prioridad'] = pd.cut(
        df_plan_final['score_prioridad_proactivo'],
        bins=[-0.01, 0.33, 0.66, 1.0],
        labels=['Baja', 'Media', 'Alta']
    )

    # Calcular Backorder
    df_plan_final['Es_backorder'] = df_plan_final['Fecha'] < hoy
    df_backorder = df_plan_final[df_plan_final['Es_backorder']].groupby('Numero de parte')['Saldo'].sum().reset_index()
    df_backorder = df_backorder.rename(columns={'Saldo':'Backorder'})

    df_plan_final = df_plan_final.merge(df_backorder, on='Numero de parte', how='left')
    df_plan_final['Backorder'] = df_plan_final['Backorder'].fillna(0)

    return df_plan_final

